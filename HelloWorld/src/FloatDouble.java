public class FloatDouble {
    public static void main(String[] args) {
        float radius = 10.8F;
        float pi = 3.14F;
        float area = pi * radius * radius;
        System.out.println(area);

        int helen = 28;
        int max = 42;
        int dany = 12;
        int liza = 17;
        int sum = helen + max + dany + liza;
        double average = sum / 4.0;
        System.out.println(average);
    }
}
